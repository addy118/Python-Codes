a="1"
b="2"
c= "aditya "
d= "kirti"
e=1
f=1.2

print(a+b) # appends both the strings
print(c+d)

# explicit TypeCasting
print(int(a)+int(b)) # adds the integer values of the strings

# implicit TypeCasting aka TypeConversion
print(e+f) # as float has higher order than the integer, the final value is in float