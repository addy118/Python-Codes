print("hello", "world", sep="-") 
# sep is used to seperate the two objects written in one print function with the specified seperator

print("hello", end="-")
# end joins the print function in the next line 
# the default end value is \n which prints the next print function value in new line  
print("world")

print(''' 
this is an example of 
multi-line string for printing
''') 
# also triple double inverted commas can be used 

print("hello")
print("world")

print("hello \nworld")